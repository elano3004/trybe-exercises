// Programa calculadora

let num1 = 5;
let num2 = 10;

// Primeiro Soma; 
let soma = num1+num2;
console.log('Soma de ' +num1+ ' + ' +num2+  ' é ' +soma);

// Primeiro Subtração; 
let subtracao = num1-num2;
console.log('Subtração de ' +num1+ ' - ' +num2+  ' é ' +subtracao);

// Primeiro Multiplicaçao; 
let multiplicacao = num1*num2;
console.log('Multiplicação de ' +num1+ ' * ' +num2+  ' é ' +multiplicacao);

// Primeiro Divisão; 
let divisao = num1/num2;
console.log('Divisão de ' +num1+ ' / ' +num2+  ' é ' +divisao);

// Primeiro Modulo; 
let modulo = num1%num2;
console.log('O resto da divisão de ' +num1+ ' % ' +num2+  ' é ' +modulo);